export function useTranslation(locale = 'pt') {
  return {
    t: (key) => {
      const keys = key.split('.');
      let value = translations[locale];
      
      for (const k of keys) {
        value = value?.[k];
      }
      
      return value || key;
    },
    locale
  };
}
